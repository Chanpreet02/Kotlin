/*
(>)  ||	greater than  ||	x > y
(<)  ||	less than  ||	x < y
(>=)  ||	greater than or equal to  ||	x >= y
(<=)  ||	less than or equal to	  ||  x <= y
(==)  ||	is equal to	  ||  x == y
(!=)  ||	not equal to  ||	x != y
*/

fun main(){
  val x: Int = 50
   val y: Int = 25

   println("x > y = " +  (x > y))
   println("x < y = " +  (x < y))
   println("x >= y = " +  (x >= y))
   println("x <= y = " +  (x <= y))
   println("x == y = " +  (x == y))
   println("x != y = " +  (x != y))

}
